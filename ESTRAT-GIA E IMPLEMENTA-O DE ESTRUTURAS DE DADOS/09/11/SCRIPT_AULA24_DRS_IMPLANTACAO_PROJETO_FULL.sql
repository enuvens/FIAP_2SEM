       FUNC.NOME "FUNCIONÁRIO",
       FUNC.GERENTE  "COD. GERENTE",
       GER.NOME  "GERENTE"
FROM   T_AULA16_2_FUNCIONARIO FUNC INNER JOIN  T_AULA16_2_FUNCIONARIO GER
       ON (GER.MATRICULA = FUNC.GERENTE);


